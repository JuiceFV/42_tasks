int	ft_iterative_factorial(int nb)
{
	int	result;
	int	i;

	result = 1;
	i = 1;
	if (nb < 0 || nb > 12)
		return (0);
	while (i < nb)
	{
		result *= (++i);
	}
	return (result);
}
