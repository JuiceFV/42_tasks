#include <stdlib.h>

char		*ft_strdup(char *src)
{
	int		i;
	char	*temp;

	i = 0;
	while (src[i] != '\0')
		i++;
	temp = (char*)malloc(sizeof(char) * i);
	i = 0;
	while (src[i] != '\0')
	{
		temp[i] = src[i];
		i++;
	}
	temp[i] = '\0';
	return (temp);
}
