int	ft_power(int a, unsigned int b)
{
	if (b == 0)
		return (1);
	else if (b % 2 == 0)
		return (ft_power(a, b / 2) * ft_power(a, b / 2));
	else
		return (a * ft_power(a, b / 2) * ft_power(a, b / 2));
}
